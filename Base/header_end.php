            item.addEventListener('click', function() {
                navItems.forEach(nav => nav.classList.remove('active'));
                this.classList.add('active');
            });
        });
    });
    document.getElementById('jobSearchForm').addEventListener('submit', function(e) {
        e.preventDefault();
        const keyword = document.getElementById('searchItem').value;
        const queryParams = new URLSearchParams();
        if (keyword) queryParams.append('keyword', keyword);
        queryParams.append('search', 1);
        window.location.href = `jobs-grid?${queryParams.toString()}`;
    });
</script>
